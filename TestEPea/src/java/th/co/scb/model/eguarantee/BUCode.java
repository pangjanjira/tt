public class BUCode {
	
	private String buCode;
	private String buDesc;
	
	/**
	 * @return the buCode
	 */
	public String getBuCode() {
		return buCode;
	}
	/**
	 * @param buCode the buCode to set
	 */
	public void setBuCode(String buCode) {
		this.buCode = buCode;
	}
	/**
	 * @return the buDesc
	 */
	public String getBuDesc() {
		return buDesc;
	}
	/**
	 * @param buDesc the buDesc to set
	 */
	public void setBuDesc(String buDesc) {
		this.buDesc = buDesc;
	}
	
	
}
